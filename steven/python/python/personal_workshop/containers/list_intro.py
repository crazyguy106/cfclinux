#!/usr/bin/python3

# Lists helps us to store data

# An empty list
lst = []
print(type(lst), lst)

# list with numbers
list_with_num = [1,2,3,4,5]
print(type(list_with_num), list_with_num)

# list with data that have different data types
list_with_anything = [1, 'a', '2', 10.5]
print(type(list_with_anything), list_with_anything)
