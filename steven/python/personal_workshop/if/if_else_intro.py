# A simple if statement! (Remember to cover indentation here!)
if is_raining:
	print('I am going to stay home and sleeeeeeeeeep')
# ================================
	
# An if-else statement!
if is_raining:
	print('I am going to stay home and sleeeeeeeeeep')
else:
	print('Go for a run!')
