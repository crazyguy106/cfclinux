#!/usr/bin/python3

# list to iterate over
subject_list = ['Math', 'English', 'Chinese', 'Chemistry', 'Physics']

# simple loop over container
for subject in subject_list:
	print(subject)
	
# Transform each item in list over for loop!
for subject in subject_list:
	subject_uppercased = subject.upper()
	print(subject_uppercased)
